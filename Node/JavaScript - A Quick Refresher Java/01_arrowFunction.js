//Arrow function
let sum = (a, b) => {
    return a + b;
}

console.log(sum(6, 4))

//If function has one line in body
add = (a,b) => a+b;
console.log(add(4,5))

//If function has one argument (No need of bracket -> ())
const addOne = a => a+1;
console.log(addOne(2))

